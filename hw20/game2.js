document.addEventListener("DOMContentLoaded", () => {
    alert("У королівстві, де легенди оживають, молодий оруженосець Люк мріє стати рицарем. Він служить Сір Роберту, рицарю, який вважає, що честь — це найвища добродійність.")
    
    const choice1 = confirm("Сір Роберт ставить перед Люком випробування. Оберіть 'OK' або 'Скасувати'.");

    if (choice1) {
        const forestChoice = prompt("Люк зустрічає чарівницю. Оберіть варіант: \n1. Прийняти зілля \n2. Відмовитися \n3. Піти через село \n4. Відправитися до замку дракона");

        switch (forestChoice) {
            case '1':
                const pledgeChoice = confirm("Люк отримує надприродну силу, але залишається в боргу перед чарівницею. Продовжити шлях?");
                pledgeChoice ?
                alert("Люк продовжує свій шлях з силою та боргом.") :
                alert("Люк обирає чесний шлях, відмовляючись від легкого успіху.");

                break;
            case '2':
                alert("Люк вибирає чесний шлях, відмовляючись від легкого успіху.");
                break;
            case '3':
                const villageChoice = confirm("Люк допомагає селянам, які потерпають від голови розбійників. Використати силу чи ні?");
                villageChoice ?
                alert("Люк використовує силу для захисту селян, але отримує поранення.") :
                alert("Люк вирішує конфлікти миром, заробляючи повагу як лідера.");

                break;
            case '4':
                const dragonChoice = confirm("Люк вирішує випробувати себе, викликаючи дракона на бій. Боротися чи намагатися перемовити?");
                dragonChoice ?
                alert("Люк гине, але його вчинок вважається героїчним.") :
                alert("Люк дізнається, що дракон не хоче воювати, і вони досягають миру.");
                
                break;
            default:
                alert("Невірний вибір. Люк обирає інший шлях.");
        }
    } else {
        alert("Люк обирає інший шлях, не вирушаючи в ліс.");
    }


    const choice2 = confirm("Після повернення з подорожей Люка викликали на турнір. Оберіть 'OK' або 'Скасувати'.");

    if (choice2) {
        const tournamentChoice = prompt("Оберіть варіант турніру: \n1. Боротьба за честь \n2. Допомога іншому учаснику \n3. Відмова від бою");

        switch (tournamentChoice) {
            case '1':
                alert("Люк демонструє свої навички, здобуваючи повагу за честь.");
                break;
            case '2':
                alert("Люк допомагає пораненому рицарю, показуючи своє серце.");
                break;
            case '3':
                alert("Люк відмовляється боротися, стверджуючи, що справжній рицар знає, коли не варто воювати.");
                break;
            default:
                alert("Невірний вибір. Люк залишає турнір без рішення.");
        }
    } else {
        alert("Люк вирішує не брати участь в турнірі.");
    }

    const choice3 = confirm("Король, вражений діями Люка, пропонує йому останнє завдання. Оберіть 'OK' або 'Скасувати'.");

    if (choice3) {
        const kingTask = prompt("Оберіть завдання короля: \n1. Захистити королівство \n2. Пошук зниклої реліквії");

        switch (kingTask) {
            case '1':
                alert("Люк веде війська проти ворога, демонструючи свою відвагу.");
                break;
            case '2':
                alert("Люк вирушає за древньою реліквією, що може врятувати королівство.");
                break;
            default:
                alert("Невірний вибір. Люк отримує інше завдання від короля.");
        }
    } else {
        alert("Люк відмовляється від завдання короля.");
    }

    alert("Фінал! Дякую за проходження.")
});